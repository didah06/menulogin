public class daftarsantri extends AppCompatActivity {

    private EditText txtid, txtnama;
    private String id_plp;


    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_daftarsantri);

        Intent intent = getIntent();
        id_plp = intent.getStringExtra(konfigurasi.EMP_ID);
        txtid = findViewById(R.id.editTextId);
        txtnama = findViewById(R.id.editTextName);
        
        txtid.setText(id_plp);
        getdataSantri();
    }

    private void getdataSantri() {
        class GetEmployee extends AsyncTask<Void,Void,String> {
            ProgressDialog loading;
            @Override
            protected void onPreExecute() {
                super.onPreExecute();
                loading = ProgressDialog.show(daftarsantri.this,"Fetching...","Wait...",false,false);
            }

            @Override
            protected void onPostExecute(String s) {
                super.onPostExecute(s);
                loading.dismiss();
                showEmployee(s);
            }

            @Override
            protected String doInBackground(Void... params) {
                RequestHandler rh = new RequestHandler();
                String s = rh.sendGetRequestParam(konfigurasi.URL_GET_EMP,id_plp);
                return s;
            }
        }
        GetEmployee ge = new GetEmployee();
        ge.execute();
    }

    private void showEmployee(String json){
        try {
            JSONObject jsonObject = new JSONObject(json);
            JSONArray result = jsonObject.getJSONArray(konfigurasi.TAG_JSON_ARRAY);
            JSONObject c = result.getJSONObject(0);
            String name = c.getString(konfigurasi.TAG_NAMA);

            txtnama.setText(name);

        } catch (JSONException e) {
            e.printStackTrace();
        }
    }
    }
